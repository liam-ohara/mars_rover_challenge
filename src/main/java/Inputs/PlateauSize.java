package Inputs;

public class PlateauSize {
    private int xSize;
    private int ySize;

    public PlateauSize(int xSize, int ySize) {
        this.xSize = xSize;
        this.ySize = ySize;
    }

    public int getXSize() {
        return xSize;
    }

    public int getYSize() {
        return ySize;
    }
}
